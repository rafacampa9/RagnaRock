    @Override
    public void paint (Graphics g){
        super.paint(g);
        g.setColor(Color.getHSBColor(250, 242, 186));
        g.fillRect(100, 170,300, 300);
        g.setColor(Color.black);
        g.drawRect(100, 170, 300, 300);
        
        int row=0, col=0;
        for (int i = 0; i < aforo; i++){
            if (i>0 && i%5==0){
                row++;
                col = 0;
            }
            int x = 100 + 60*col;
            int y = 410 - 60*row;
                
                
            if (row%2!=0){
                if (col%2==0)
                    g.setColor(Color.black);
                else
                    g.setColor(Color.red);
            } else {
                if (col%2==0)
                    g.setColor(Color.red);
                else
                    g.setColor(Color.black);
            }

            g.fillOval(x, y, 60, 60);
            col++;
            
        }
    }

